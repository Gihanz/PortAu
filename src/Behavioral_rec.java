    private void Table_BehavioralMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_BehavioralMouseClicked

            int row=Table_Behavioral.getSelectedRow();
            String Table_click=(Table_Behavioral.getModel().getValueAt(row,0).toString());

        try{
            String sql="select image from behavioralrecords where no='"+Table_click+"' ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();

            if(rs.next()){
                byte[]imagedata=rs.getBytes("image");
                format=new ImageIcon(imagedata);
                image.setIcon(format);
            }

        }catch(Exception e){
            e.printStackTrace();

        }



        try{
            
            String sql="select * from Behavioralrecords where no= '"+Table_click+" '";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();

            if(rs.next()){
                String add1=rs.getString("no");
                txt_No.setText(add1);
                String add2=rs.getString("elephantid");
                txt_Elephantid.setText(add2);
                String add3=rs.getString("elephantname");
                txt_Elephantname.setText(add3);
                Date add4=rs.getDate("date");
                txt_Date.setDate(add4);
                String add5=rs.getString("specificbehaviour");
                txt_Specific.setText(add5);
                String add8=rs.getString("remarks");
                txt_Remarks.setText(add8);

            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_Table_BehavioralMouseClicked

