    private void Cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Cmd_saveActionPerformed

        String pass_id=txt_Elephantid.getText();
        try{
            String sql="INSERT INTO Behavioralrecords (no,elephantid,elephantname,date,specificbehaviour,remarks,image) VALUES (?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);

            pst.setString(1, txt_No.getText());
            pst.setString(2, txt_Elephantid.getText());
            pst.setString(3, txt_Elephantname.getText());
            pst.setString(4, ((JTextField) txt_Date.getDateEditor().getUiComponent()).getText());
            pst.setString(5, txt_Specific.getText());
            pst.setString(6, txt_Remarks.getText());
            pst.setBytes(7, elephant_image);

            pst.execute();

            JOptionPane.showMessageDialog(null,"New Behavioral Record Saved");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

        Update_table(pass_id);
    }//GEN-LAST:event_Cmd_saveActionPerformed

